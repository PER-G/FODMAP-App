export function render(container) {
  clearEl(container);
  const wrapper = el('div', { className: 'more-menu' });

  for (const item of ITEMS) {
    if (item.key === 'moreDisclaimer') {
      const btn = el('div', { className: 'more-menu-item', style: 'cursor:pointer;' });
      btn.innerHTML = item.icon;
      btn.appendChild(el('span', {}, t(item.key)));
      btn.addEventListener('click', () => {
        document.getElementById('disclaimer-modal').hidden = false;
      });
      wrapper.appendChild(btn);
    } else {
      const link = el('a', { className: 'more-menu-item', href: item.href });
      link.innerHTML = item.icon;
      link.appendChild(el('span', {}, t(item.key)));
      wrapper.appendChild(link);
    }
  }

  // Version
  wrapper.appendChild(el('p', { className: 'text-xs text-muted text-center mt-lg' }, `${t('version')} 1.0.0`));

  container.appendChild(wrapper);
}
